import { Component, OnInit } from '@angular/core';
import { ActivatedRoute, Router } from '@angular/router';
import { FormBuilder, Validators } from '@angular/forms';
import { MatDialog } from '@angular/material/dialog';

import { TimesheetService } from '../timesheet.service';
import { ITimesheet } from '../itimesheet';
import { BaseDetailsComponent, Globals, PickerDialogService, ErrorService } from 'src/app/common/shared';
import { GlobalPermissionService } from 'src/app/core/global-permission.service';

import { UsersService } from 'src/app/admin/user-management/users/users.service';

@Component({
  selector: 'app-timesheet-details',
  templateUrl: './timesheet-details.component.html',
  styleUrls: ['./timesheet-details.component.scss'],
})
export class TimesheetDetailsComponent extends BaseDetailsComponent<ITimesheet> implements OnInit {
  title = 'Timesheet';
  parentUrl = 'timesheet';
  constructor(
    public formBuilder: FormBuilder,
    public router: Router,
    public route: ActivatedRoute,
    public dialog: MatDialog,
    public global: Globals,
    public timesheetService: TimesheetService,
    public pickerDialogService: PickerDialogService,
    public errorService: ErrorService,
    public usersService: UsersService,
    public globalPermissionService: GlobalPermissionService
  ) {
    super(formBuilder, router, route, dialog, global, pickerDialogService, timesheetService, errorService);
  }

  ngOnInit() {
    this.entityName = 'Timesheet';
    this.setAssociations();
    super.ngOnInit();
    this.setForm();
    this.getItem();
    this.setPickerSearchListener();
  }

  setForm() {
    this.itemForm = this.formBuilder.group({
      id: [{ value: '', disabled: true }, Validators.required],
      notes: [''],
      periodendingdate: [''],
      periodendingdateTime: [''],
      userid: [''],
      usersDescriptiveField: [''],
    });

    this.fields = [
      {
        name: 'notes',
        label: 'notes',
        isRequired: false,
        isAutoGenerated: false,
        type: 'string',
      },
      {
        name: 'periodendingdate',
        label: 'periodendingdate',
        isRequired: false,
        isAutoGenerated: false,
        type: 'date',
      },
      {
        name: 'periodendingdateTime',
        label: 'periodendingdate Time',
        isRequired: false,
        isAutoGenerated: false,
        type: 'time',
      },
    ];
  }

  onItemFetched(item: ITimesheet) {
    this.item = item;
    this.itemForm.patchValue(item);
    this.itemForm.get('periodendingdate').setValue(item.periodendingdate ? new Date(item.periodendingdate) : null);
    this.itemForm
      .get('periodendingdateTime')
      .setValue(this.timesheetService.formatDateStringToAMPM(item.periodendingdate));
  }

  setAssociations() {
    this.associations = [
      {
        column: [
          {
            key: 'timesheetid',
            value: undefined,
            referencedkey: 'id',
          },
        ],
        isParent: true,
        table: 'timesheetdetails',
        type: 'OneToMany',
        label: 'timesheetdetails',
      },
      {
        column: [
          {
            key: 'timesheetid',
            value: undefined,
            referencedkey: 'id',
          },
        ],
        isParent: true,
        table: 'timesheetstatus',
        type: 'OneToMany',
        label: 'timesheetstatus',
      },
      {
        column: [
          {
            key: 'userid',
            value: undefined,
            referencedkey: 'id',
          },
        ],
        isParent: false,
        table: 'users',
        type: 'ManyToOne',
        label: 'users',
        service: this.usersService,
        descriptiveField: 'usersDescriptiveField',
        referencedDescriptiveField: 'id',
      },
    ];

    this.childAssociations = this.associations.filter((association) => {
      return association.isParent;
    });

    this.parentAssociations = this.associations.filter((association) => {
      return !association.isParent;
    });
  }

  onSubmit() {
    let timesheet = this.itemForm.getRawValue();
    timesheet.periodendingdate = this.dataService.combineDateAndTime(
      timesheet.periodendingdate,
      timesheet.periodendingdateTime
    );
    delete timesheet.periodendingdateTime;
    super.onSubmit(timesheet);
  }
}
